                                break;
                            }
                        }
                    }

                    if (check0 == 1) {
                        val = evalMy1(str2); // ( ) の処理をし数値をもとめる
                    } else {
                        val = double.Parse(str2); // 文字を数値に変換
                    }
                    if (n2 - 8 >= 0) {
                        string str1 = equ.Substring(n2 - 8, n2 - (n2 - 8));
                        if (str1 == "Math.Sin") {
                            val = Math.Sin(val);
                            equ = equ.Replace("Math.Sin(" + str2 + ")", "" + val);
                            n2 -= 8;
                        } else if (str1 == "Math.Cos") {
